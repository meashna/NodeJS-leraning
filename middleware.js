const express = require('express')
const app = express()

const myLogger = function (req, res, next) {
  console.log('LOGGED')
  next()
}

app.use(myLogger)

app.get('/', (req, res) => {
  res.send('Hello World!')
})

const PORT=process.env.PORT||5000

app.listen(PORT,() => console.log(`Server running in ${PORT}`))